# include <bits/stdc++.h>
using namespace std;

int arr[], target, size;

int main() {
    cin << arr[];
    cin << target;
    for (int i=0; i<=size; ++i) {
        for (int j=0; i<=size; ++j) {
            if (arr[i]+arr[j]==target) {
                cout << "Output: [" << i << ", " << j << "]" << endl;
            }
        }
    }
    return 0;
}